CREATE TABLE CUPOM_CATEGORIA(
    CODIGO INT,
    ID_CATEGORIA INT, 
    CONSTRAINT FK_CUPOMCATEGRIA_IDCUPOM FOREIGN KEY (CODIG) REFERENCES CUPOM_DESCONTO(CODIGO),
    CONSTRAINT FK_CUPOMCATEGORIA_IDCATEGORIA FOREIGN KEY (ID_CATEGORIA) REFERENCES CATEGORIA(CODIGO),
    CONSTRAINT PK_CUPOM_CATEGORIA PRIMARY KEY (CODIG, ID_CATEGORIA)
);